    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int i = jTable1.getSelectedRow(); //baris kesatu
        if(i == -1){
            return;
        }
        
        String kode = (String) tabelmodel.getValueAt(i, 0); //kolom ke 0
        txt_kode.setText(kode);
        String nama = (String) tabelmodel.getValueAt(i, 1); //kolom ke 1
        txt_nama.setText(nama);
        int stok = (int) tabelmodel.getValueAt(i, 2);
        String stk = String.valueOf(stok);
        txt_stok.setText(stk);
        int harga = (int) tabelmodel.getValueAt(i, 3);
        String hrg = String.valueOf(harga);
        txt_harga.setText(hrg);
    }//GEN-LAST:event_jTable1MouseClicked

