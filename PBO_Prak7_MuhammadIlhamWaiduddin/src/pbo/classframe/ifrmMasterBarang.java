    public void loadData(){
        ModelTabelBarang();
        ClassBarang cb = new ClassBarang();
        cb.getDataBarang();
        int ndata = cb.getNumberDataBarang();
        Object[][] data = cb.getAllDataBarang();
        Object[] data1 = new Object[4];
        for (int i = 0; i < ndata; i++){
            for (int j = 0; j < 4;j++){
                data1[j] = data[i][j];
                System.out.println(data1[j]);
            }
            tabelmodel.addRow(data1);
        }
    }
    
    public void loadData(String key){
        ModelTabelBarang();
        ClassBarang cb = new ClassBarang();
        cb.getDataBarang(key);
        int ndata = cb.getNumberDataBarang();
        Object[][] data = cb.getAllDataBarang();
        Object[] data1 = new Object[4];
        for (int i = 0; i < ndata; i++){
            for (int j = 0; j < 4;j++){
                data1[j] = data[i][j];
                System.out.println(data1[j]);
            }
            tabelmodel.addRow(data1);
        }
    }
    
    public void ModelTabelBarang(){
        tabelmodel = new DefaultTableModel();
        jTable1.setModel(tabelmodel);
        tabelmodel.addColumn("Kode");
        tabelmodel.addColumn("Nama");
        tabelmodel.addColumn("Stok");
        tabelmodel.addColumn("Harga");
        tabelmodel.getDataVector().removeAllElements();
        tabelmodel.fireTableDataChanged();
    }

    public void clearData(){
        txt_kode.setText("");
        txt_nama.setText("");
        txt_stok.setText("");
        txt_harga.setText("");
    }
    

